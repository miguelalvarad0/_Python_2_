import math


class Trig():
    
    def __init__(self):
        self.pi = math.pi
    
    def sin():
        sin = math.sin(math.pi)
        return(sin)   
    
    def cos():
        cos = math.cos(math.pi)
        return(cos)
        #print(f"El seno de pi es: {cos}")

    def tan():
        tan = math.tan(math.pi)
        return(tan)
        #print(f"El seno de pi es: {tan}")
